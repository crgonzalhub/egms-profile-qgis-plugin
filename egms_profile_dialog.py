class EGMSPluginDialog(QtWidgets.QDialog, FORM_CLASS):
    def __init__(self, iface, parent=None):
        super(EGMSPluginDialog, self).__init__(parent)
        self.setupUi(self)
        self.iface = iface
        self.egms_data = []
        self.layer = None
        self.fig = None
        self.profile_data = []

